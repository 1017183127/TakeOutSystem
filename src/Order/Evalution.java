package Order;

public class Evalution {
	private int oid;
	private int uid;
	private String reva;
	private String deva;
	private String ueva;
	
	public int getOid() {
		return oid;
	}
	public void setOid(int oid) {
		this.oid = oid;
	}
	public String getReva() {
		return reva;
	}
	public void setReva(String reva) {
		this.reva = reva;
	}
	public String getDeva() {
		return deva;
	}
	public void setDeva(String deva) {
		this.deva = deva;
	}
	public String getUeva() {
		return ueva;
	}
	public void setUeva(String ueva) {
		this.ueva = ueva;
	}
	public int getUid() {
		return uid;
	}
	public void setUid(int uid) {
		this.uid = uid;
	}
}
